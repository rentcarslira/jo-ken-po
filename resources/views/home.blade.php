      <h1>Pedra, papel e tesoura</h1>
      <hr>
        <p>Origem: Wikipédia, a enciclopédia livre.
        Pedra, papel e tesoura, também chamado em algumas regiões do Brasil de jokenpô[1][2] (do japonês じゃんけんぽん, jankenpon) é um jogo de mãos recreativo e simples para duas ou mais pessoas, que não requer equipamentos nem habilidade.

        O jogo é frequentemente empregado como método de seleção (como na escolha de equipes para a prática desportiva, por exemplo), assim como lançar moedas, jogar dados, entre outros. No entanto, diferentemente desses métodos que se baseiam exclusivamente em sorte, pedra-papel-tesoura pode ser jogado com um pouco de habilidade. Principalmente se o jogo se estender por vários turnos com o mesmo jogador, este pode reconhecer e explorar a lógica do comportamento do adversário (perceber e anteceder as jogadas do adversário).
        </p>
        <br>
        <h3>Regras</h3>
        <p>Rock-paper-scissors (rock).png	Rock-paper-scissors (paper).png	Rock-paper-scissors (scissors).png
        Cada um dos três sinais básicos (da esquerda para a direita: pedra, papel e tesoura).
        No Janken-pon, os jogadores devem simultaneamente esticar a mão, na qual cada um formou um símbolo (que significa pedra, papel ou tesoura). Então, os jogadores comparam os símbolos para decidir quem ganhou, da seguinte forma:

        Pedra ganha da tesoura (amassando-a ou quebrando-a).
        Tesoura ganha do papel (cortando-o).
        Papel ganha da pedra (embrulhando-a).
        A pedra é simbolizada por um punho fechado; a tesoura, por dois dedos esticados; e o papel, pela mão aberta. Caso dois jogadores façam o mesmo gesto, ocorre um empate, e geralmente se joga de novo até desempatar. Este jogo possui uma única regra: não é permitido mostrar pedra duas vezes seguidas.
        </p>
        <br>
        <h3>História</h3>
        <p>
        San Sukumi Ken (1809)
        No Japão, Pedra, papel, tesoura é chamado simplesmente de "Janken" . Acredita-se que Janken basea-se em dois jogos Ken mais antigos, "Suu Ken" e "San Sukumi Ken". O San Sukumi Ken existiu no Japão desde tempos antigos, e o Suu Ken foi importado da China no fim do século XVII. Os jogos Ken começaram a aumentar em popularidade em meados do século XIX. Acredita-se que Janken foi inventado no fim do século XIX, julgando fontes textuais da época sobre jogos Ken.

        Em 2012, cientistas da Universidade de Tóquio criaram um robô (uma espécie de mão robótica com câmeras de alta velocidade), que nunca perde no Janken-pon. O "truque" tem a ver com um mapeamento da mão e uma prévia visualização através de câmeras a fim de detectar o início do movimento dos dedos.[3]
        </p>

        <h3>Variações</h3>
        <p>
        O jogo é conhecido em vários lugares do mundo, e é acompanhado de "gritos" ou rimas diferentes: Nos países lusófonos, os jogadores geralmente dizem "Pedra, Papel, Tesoura" antes de jogar. Em algumas regiões do Brasil usa-se "Janken-pon" devido à influência de imigrantes japoneses, sendo que pedra, papel e tesoura não é o nome usual do jogo em São Paulo. No Rio Grande do Sul, no entanto, este jogo tem o nome alternativo de "escoteirinho", que a palavra que se fala antes de jogar.[4]

        Em alguns países, substituem-se a pedra, a tesoura e o papel por outros símbolos. Por exemplo, na Índia usam-se "homem", "arma" e "tigre", sendo que o homem usa a arma, a arma mata o tigre, e o tigre mata o homem. Também é comum a criação de vários outros símbolos, de acordo com a cultura popular, as situações em que o jogo é disputado e muitas outras variáveis. O que vale é a imaginação livre dos jogadores.

        Outra característica que difere os jogadores de pedra, papel, tesoura ao redor do mundo é que alguns povos jogam assim que a última palavra do "grito" é dita, e outros dizem as palavras e então jogam. Essa diferença de tempo pode levar à vitória por parte de um oponente que faça sua jogada depois, caso ambos não estejam de comum acordo.

        Há ainda outras variações que incluem mais de três sinais básicos. Estas diferem-se, por um único sinal ganhar e perder de mais de um outro sinal. Por exemplo, pode-se incluir fogo, que ganha do papel como a tesoura; água, que ganha da pedra (origina-se do provérbio "Água mole em pedra dura tanto bate até que fura"); e outros ainda como agulha e linha. Em geral, costuma-se organizar bem antes de utilizar mais de três sinais, para que o jogo continue equilibrado. Por exemplo, estaria errado em jogar com pedra, papel, tesoura, fogo e água, pois, nesse caso, a água venceria de todos: apagaria o fogo, furaria a pedra, enferrujaria a tesoura, e molharia ou desmancharia o papel.

        O americano Sam Kass criou uma variação que foi popularizada no seriado The Big Bang Theory chamada Pedra, papel, tesoura, lagarto, Spock. Na variante acrescenta-se: pedra esmaga lagarto, lagarto envenena Spock, Spock quebra tesoura, tesoura decapita lagarto, lagarto come papel, papel contesta Spock, Spock vaporiza rocha. O lagarto é representado unindo-se as pontas de todos os dedos da mão, enquanto Spock é representado pela saudação vulcana. [5]
        </p>

        <h3>Torneios</h3>
        <p>
        Ver artigo principal: Lista de competições de pedra, papel e tesoura
        Torneios de Pedra, Papel, Tesoura são disputados em alguns países. Há uma associação mundial de jogadores, a "World Rock Paper Scissors Society" (WRPS), que instituiu um campeonato mundial em 2002. O campeonato é conhecido por seus grandes prêmios em dinheiro e pelos excêntricos competidores, e é televisionado nos Estados Unidos pela FOX Sports Net. O campeonato é disputado anualmente em Toronto, no Canadá.

        Em junho de 2008, Sean Dears recebeu um prêmio de 50 mil dólares ao sagrar-se campeão americano nesse jogo.[6]
        </p>

